# 复数型（complex） 例：6+4j、-5+12j、98+9j
# complex()函数可以使用参数real + imag*j方式创建一个复数

print(complex(9))  # (9+0j)

print(complex('3+89j'))  # (3+89j)

print(complex(4,5)) # (4+5j)

K=[1,2,3,4,5,6]
for i in K:
    print(complex(i,7))

